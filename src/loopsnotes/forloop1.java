package loopsnotes;

public class forloop1 {

	public static void main(String[] args) {
		
		for(int i=0;i<5;i++	) {
			System.out.println("this is i value " + i);
			
		}
		
		// 1. i=0	0<5   true	 this is i value 0
		// 2. i=1	1<5	  true	 this is i value 1
		// 3. i=2	2<5   true	 this is i value 2
		// 4. i=3	3<5	  true	 this is i value 3
		// 5. i=4	4<5   true	 this is i value 4
		// 6. i=5	5<5	  false	 Condition is false,so controller comeout from the loop and execute the next line code.
	}
	
	
}
