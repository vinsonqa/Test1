package datatype;

public class NamingConvention {

	/*
	 * NamingConvention
	 *  ## Class name:-
	 *  Every word first letter should be in uppercase letter
	 *  ex. FirstJavaProgram
	 *  
	 *  ## package name:-
	 *  All letter should be in lower case letter
	 *  ex. javaautomation
	 *  
	 *  ## Variable Name:-
	 *  First word first letter should be small and after every word first letter should be in upper case letter
	 *  ex. customerCompanyName
	 *  
	 *  ## Method:- 
	 *  First word first letter should be small and after every word first letter should be in upper case letter
	 *  in last we used "()" and "{ }"
	 *  ex. makePayment() {
	 *  }
	 * 
	 * ## Java comment:-
	 * 1. Single line comment:- //
	 * 2. Multi line comment:- /*....... */
	 
	
	
	
}
